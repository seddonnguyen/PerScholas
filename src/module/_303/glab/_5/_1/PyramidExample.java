package module._303.glab._5._1;

public class PyramidExample {
    public static void main(String[] args) {
        // Example 4: Pyramid
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();//new line
        }
    }
}
